tea_size = input("Enter the size of the cup: ").lower()
if tea_size == "small":
    print(f"Cost price of {tea_size} is Rs 10")
elif tea_size == "medium":
    print(f"Cost price of {tea_size} is Rs 15")
elif tea_size == "large":
     print(f"Cost price of {tea_size} is Rs 20")
else:
    print(f"Unknown cup size")


 